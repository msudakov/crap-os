const BACKGROUND_COLOR: u32 = 0x00000000;
const FONT_COLOR: u32 = 0x004AF262;
const BOUNDARY_COLOR: u32 = 0x00000080;
const FONT_WIDTH: u32 = 8;
const FONT_HEIGHT: u32 = 12;
const PAD_LEFT: u32 = FONT_WIDTH;
const PAD_RIGHT: u32 = FONT_WIDTH;
const PAD_TOP: u32 = FONT_HEIGHT;
const PAD_BOTTOM: u32 = FONT_HEIGHT;

#[repr(C)]
pub struct FramebufferInfo {
    pub framebuffer_addr: u64,
    pub framebuffer_width: u32,
    pub framebuffer_height: u32,
    pub framebuffer_pitch: u32,
    pub framebuffer_bpp: u32,
}

pub struct FramebufferWriter {
    pub framebuffer: *mut u32,
    pub fb_width: u32,
    pub fb_height: u32,
    current_x_pos: u32,
    current_y_pos: u32,
    vertical_cutoff: u32,
}

/// TODO: This works with text and basic i32 numbers for concatenation, but it
/// crashes at 0xAA020 (which is below kernel base address) for most other
/// format strings, with exception of Exception Type - 06(#UD - Invalid Opcode).
/// Need to debug.
impl fmt::Write for FramebufferWriter {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        self.draw_string(s);
        Ok(())
    }
}

impl FramebufferWriter {
    /// Instantiates a new `FramebufferWriter`.
    ///
    /// # Arguments
    ///
    /// * `framebuffer` - Pointer to frame buffer to draw in.
    /// * `fb_width` - Width of the specified frame buffer.
    /// * `fb_height` - Height of the specified frame buffer.
    pub fn new(framebuffer: *mut u32, fb_width: u32, fb_height: u32) -> Self {
        Self {
            framebuffer,
            fb_width,
            fb_height,
            current_x_pos: PAD_LEFT,
            current_y_pos: PAD_TOP,
            vertical_cutoff: fb_height - PAD_BOTTOM,
        }
    }

    /// Clears out and prepares a new blank screen for a printing session.
    ///
    /// # Safety
    /// 
    /// Performs a `write_volatile` on the frame buffer memory location.
    pub fn clear_screen(&mut self) {
        let total_pixels = self.fb_width * self.fb_height;
        for i in 0..total_pixels {
            unsafe {
                self.framebuffer.offset(i as isize).write_volatile(
                    BACKGROUND_COLOR);
            }
        }

        self.current_x_pos = PAD_LEFT;
        self.current_y_pos = PAD_TOP;
    }

    /// Draws a single character into the frame buffer.
    ///
    /// # Arguments
    ///
    /// * `char` - Character to draw.
    fn draw_char(&mut self, char: u8) {
        match char {
            b'\n' => self.new_line(),  // The newline char is allowed
            char => {
                // Only handle standard printable ASCII characters (32-126)
                if char < 32 || char > 126 {
                    return;
                }
                if self.current_x_pos >= self.fb_width - PAD_LEFT - PAD_RIGHT {
                    self.new_line();
                }

                draw_char(self.framebuffer, self.fb_width, self.current_x_pos,
                    self.current_y_pos, char);

                self.current_x_pos += FONT_WIDTH;
            }
        }
    }

    /// Initializes a new line in the frame buffer.
    fn new_line(&mut self) {
        let old_line = self.current_y_pos;  // Save the old y coordinate

        self.current_y_pos += FONT_HEIGHT;  // Update the y coordinate
        self.current_x_pos = PAD_LEFT;      // Update the x coordinate

        // Default boundary lines if the screen is not wrapping
        let mut boundary_line_1 = self.current_y_pos + FONT_HEIGHT * 1;
        let mut boundary_line_2 = self.current_y_pos + FONT_HEIGHT * 2;

        if self.current_y_pos >= self.vertical_cutoff {
            // If the new effective writing line is wrapping around the screen:
            self.current_y_pos = PAD_TOP;
            boundary_line_1 = self.current_y_pos + FONT_HEIGHT * 1;
            boundary_line_2 = self.current_y_pos + FONT_HEIGHT * 2;
        }
        else if boundary_line_1 >= self.vertical_cutoff {
            // If the first boundary line is wrapping around the screen:
            boundary_line_1 = PAD_TOP;
            boundary_line_2 = boundary_line_1 + FONT_HEIGHT;
        }
        else if boundary_line_2 >= self.vertical_cutoff {
            // If the second boundary line is wrapping around the screen:
            boundary_line_2 = PAD_TOP;
        }

        // Clear out the '>' on the previous line by drawing a space char there
        draw_char(self.framebuffer, self.fb_width, 0, old_line, 32);

        // Clear boundary fill from the new (now current) line
        self.fill_line(self.current_y_pos * self.fb_width, BACKGROUND_COLOR);

        // Print '>' to the new line to show it as current
        draw_char(self.framebuffer, self.fb_width, 0, self.current_y_pos, 62);

        // Draw a two-line boundary, erasing previous content if any, on the
        // following two lines, which may wrap around the screen from the top.
        self.fill_line(boundary_line_1 * self.fb_width, BOUNDARY_COLOR);
        self.fill_line(boundary_line_2 * self.fb_width, BOUNDARY_COLOR);
    }

    /// Fills a font line in the frame buffer with the specified color.
    /// 
    /// # Arguments
    ///
    /// * `fill_start` - Y pixel coordinate to start filling at.
    /// * `color` - Color code for the filled line.
    ///
    /// # Safety
    /// 
    /// Performs a `write_volatile` on the frame buffer memory location.
    fn fill_line(&mut self, fill_start: u32, color: u32) {
        let fill_end = self.fb_width * FONT_HEIGHT + fill_start;

        for i in fill_start..fill_end {
            unsafe {
                self.framebuffer.offset(i as isize).write_volatile(color);
            }
        }
    }

    /// Draws a string into the frame buffer.
    /// 
    /// # Arguments
    ///
    /// * `str` - String text to draw.
    fn draw_string(&mut self, str: &str) {
        for char in str.bytes() {
            if char >= 32 && char <= 126 || char == b'\n' {
                self.draw_char(char);
            }
            else {
                self.draw_char(63);  // The '?' char
            }
        }
    }

    /// Prints a basic non-formatted string without a newline character.
    /// 
    /// # Arguments
    ///
    /// * `str` - String text to print.
    pub fn print(&mut self, str: &str) {
        self.draw_string(str);
    }

    /// Prints a basic non-formatted string with a newline character.
    /// 
    /// # Arguments
    ///
    /// * `str` - String text to print.
    pub fn println(&mut self, str: &str) {
        self.print(str);
        self.new_line();
    }

    /// Handles the printing of byte slices or arrays passed as slices.
    /// 
    /// # Arguments
    ///
    /// * `bytes` - Byte slice to print as string.
    /// 
    /// # Safety
    /// 
    /// The bytes passed to this function must all be printable characters.
    pub fn print_bytes(&mut self, bytes: &[u8]) {
        let byte_string: &str = unsafe {
            core::str::from_utf8_unchecked(bytes)
        };
        self.draw_string(byte_string);
    }

    /// Draws an ASCII banner of the OS.
    pub fn draw_banner(&mut self) {
        self.println("+------------------------------------------------+");
        self.println("|                                                |");
        self.println("|   #####                       #######  #####   |");
        self.println("|  #     # #####    ##   #####  #     # #     #  |");
        self.println("|  #       #    #  #  #  #    # #     # #        |");
        self.println("|  #       #    # #    # #    # #     #  #####   |");
        self.println("|  #       #####  ###### #####  #     #       #  |");
        self.println("|  #     # #   #  #    # #      #     # #     #  |");
        self.println("|   #####  #    # #    # #      #######  #####   |");
        self.println("|                                                |");
        self.println("+------------------------------------------------+");
        self.println("");
    }
}

/// Draws a single character into a frame buffer.
///
/// Draws a character at position (x, y) with given foreground and background
/// colors.
/// 
/// # Arguments
///
/// * `framebuffer` - Pointer to frame buffer to draw in.
/// * `fb_width` - Width of the specified frame buffer.
/// * `pos_x` - X coordinate of position to draw at.
/// * `pos_y` - Y coordinate of position to draw at.
/// * `char` - Character to draw.
///
/// # Safety
/// 
/// Performs a `write_volatile` on the frame buffer memory location.
fn draw_char(
    framebuffer: *mut u32,
    fb_width: u32,
    pos_x: u32,
    pos_y: u32,
    char: u8,
) {
    // Only handle standard printable ASCII characters (32-126)
    if char < 32 || char > 126 {
        return;
    }

    // Subtract the lowest character value of 32 to get its index in the array
    let font_index = (char - 32) as usize;
    let glyph = &FONT[font_index];

    // Loop through each of the 8 bytes of the bitmap index
    for row in 0..FONT_HEIGHT {
        let byte = glyph[row as usize];
        for col in 0..FONT_WIDTH {
            let pixel_x = pos_x + col;
            let pixel_y = pos_y + row;
            
            // Determine whether to draw the pixel in bg or fg color
            let color = if byte & (1 << col) != 0 {
                FONT_COLOR
            }
            else {
                BACKGROUND_COLOR
            };

            // Determine the absolute offset to draw the pixel at
            let offset = (pixel_y * fb_width + pixel_x) as isize;

            // Draw the pixel
            unsafe { framebuffer.offset(offset).write_volatile(color); }
        }
    }
}
